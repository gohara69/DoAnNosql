    public void displayDetail(int row){
        if(row >= 0){
            Vector<String> info = (Vector<String>) data.get(row);
            txtMaDV.setText(info.get(0));
            txtTenDV.setText(info.get(1));
            txtGiaDV.setText(info.get(3));
            txtMoTa.setText(info.get(2));
        }
    }
    
